<div class="p-3 mb-2 bg-secondary text-white">Преимущества SimpleCoding</div>
<div class="font-weight-normal text-justify p-3">
    В настоящее время существует достаточно много фрэймворков, как известных, так и не особо распространенных.
        <code>SimpleCoding</code> - это тот базовый функционал, который даст разработчику хороший старт в написании любого проекта.
        Основное ядро заточено под технологию HMVC, и подразумевает написание страниц в виде классов и методов.
        Давайте обратим внимание на основные моменты данного фрэймворка.
</div>

<div class="bg-light p-3 border-top border-bottom text-secondary font-weight-normal">Документация</div>
<div class="font-weight-normal text-justify p-3">
    Подробная документация к <code>SimpleCoding</code> на русском языке - еще один плюс данного фрэймворка.
        С ее помощью даже начинающий web-разработчик легко и быстро разберется в движке и начнет использовать его для своих проектов.
</div>

<div class="bg-light p-3 border-top border-bottom text-secondary font-weight-normal">ЧПУ</div>
<div class="font-weight-normal text-justify p-3">
    Движок заранее настроен на работу с человекопонятными простыми URL вида <code>http://site.com/page/method/data/data</code>
</div>

<div class="bg-light p-3 border-top border-bottom text-secondary font-weight-normal">Скорость</div>
<div class="font-weight-normal text-justify p-3">
    <code>SimpleCoding</code> достаточно быстый в работе, насколько это возможно для движков, ориентированных на <code>HMVC</code> структуру.
        Например, для полной инициализации всего фрэймворка, потребуется менее одной сотой секунды и несколько КБ оперативной памяти.
</div>

<div class="bg-light p-3 border-top border-bottom text-secondary font-weight-normal">Мультиязычность</div>
<div class="font-weight-normal text-justify p-3">
    Движок приспособлен для мультиязычных проектов. Просто включите данную опцию в конфиге проекта.
</div>

<div class="bg-light p-3 border-top border-bottom text-secondary font-weight-normal">Автолоадэр</div>
<div class="font-weight-normal text-justify p-3">
    Вам не надо подключать новые классы к проекту, все подключается автолоадэром по стандарту <code>psr-4</code>.
</div>

<div class="bg-light p-3 border-top border-bottom text-secondary font-weight-normal">Роутинг</div>
<div class="font-weight-normal text-justify p-3">
    В <code>SimpleCoding</code> Вы так же можете настроить роуты по своему усмотрению, что облегчит перенос проекта без нарушения SEO.
</div>

<div class="bg-light p-3 border-top border-bottom text-secondary font-weight-normal">Административная панель</div>
<div class="font-weight-normal text-justify p-3">
    Удобная, расширяемая под свои требования, админка. Просто напишите <code>/admin</code> после имени сайта в адресной строке
</div>

<div class="bg-light p-3 border-top border-bottom text-secondary font-weight-normal">Composer компоненты</div>
<div class="font-weight-normal text-justify p-3">
    <code>SimpleCoding</code> включает в себя <code>composer.json</code> для загрузки известных компонентов от <code>Symphony</code> и <code>Laravel</code>.
    <br /><br />
    <code>illuminate/database</code> - работа с базами данных<br />
    <code>swiftmailer/swiftmailer</code> - работа с почтой<br />
    <code>symfony/cache</code> - работа с кэшированием<br />
    <code>guzzlehttp/guzzle</code> - работа с запросами<br />
    <code>vlucas/phpdotenv</code> - работа с .env файлами
</div>

